define(['jquery'],function($){
    $('body').on('click','.jAddProType',function(){
        alert(1);
        if($(this).hasClass('open')){
            $('.desc-child').addClass('isHide');
            $(this).removeClass('open');
        }else{
            $('.desc-child').removeClass('isHide');
            $(this).addClass('open');
        }
    });
});